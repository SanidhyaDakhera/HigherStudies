; FIND NEGATIVE NUMBERS FROM SIGNED ARRAY
.MODEL SMALL
.STACK
.DATA
M2 DB 10,13, " $"
M1 DB 10,13, "NEGATIVE NO:- $"
ARRAY1 DB 50H,0AFH,25H,98H,0FEH ; INITIALIZE ARRAY
NARRAY DB 5 DUP(0) ; UNINTIALIZE ARRAY  

.CODE
DISP MACRO XX
MOV AH, 09
LEA DX,XX
INT 21H
ENDM
.STARTUP
MOV CH, 0 ; NEGATIVE COUNT 
LEA SI,ARRAY1
LEA DI,NARRAY
MOV CL, 5
BACK:
MOV DL ,[SI]
AND DL, 80H
JZ POSITIVE 
MOV BL , [SI]
MOV [DI], BL
INC DI
INC CH 
POSITIVE:
INC SI 
DEC CL
JNZ BACK
DISP M1 
LEA DI, NARRAY
BACK1:
DISP M2
MOV BH,[DI]
AND BH , 0F0H
MOV CL ,4
SHR BH ,CL
CMP BH ,9
JG AA
ADD BH, 30
JMP AA1
AA:
ADD BH,37H
AA1:
MOV DL, BH 
MOV AH,02
INT 21H
MOV BH, [DI]
AND BH, 0FH
MOV BH,[DI]
AND BH , 0FH
CMP BH ,9
JG AA2
ADD BH, 30H
JMP AA3
AA2:
ADD BH,37H
AA3:
MOV DL, BH 
MOV AH,02
INT 21H
MOV BH, [DI]
AND BH, 0FH
INC DI 
DEC CH
JNZ BACK1
.EXIT
END 
